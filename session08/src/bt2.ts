// Khai báo một biến kiểu boolean và gán giá trị cho biến đó.
// Sử dụng biến đã khai báo làm điều kiện trong câu lệnh điều kiện (if), nếu giá trị là true, in ra "Xin chào", nếu giá trị là false, in ra "Tạm biệt".
let check: boolean = true;
if (check) {
  console.log("Xin chào");
} else {
  console.log("Tạm biệt");
}